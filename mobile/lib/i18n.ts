const dictionaries: Record<Locale, Dict> = { en, hi, mr };

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let text = dictionaries[locale]?.[key] ?? dictionaries.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return text;
}

interface LocaleState {
  locale: Locale;
  setLocale: (l: Locale) => void;
}

export const useLocaleStore = create<LocaleState>()(
  persist(
    (set) => ({ locale: "en", setLocale: (l) => set({ locale: l }) }),
    { name: "gtc-locale", storage: createJSONStorage(() => AsyncStorage) }
  )
);

/** Hook returning a translate function bound to the current locale. */
export function useT() {
  const locale = useLocaleStore((s) => s.locale);
  return (key: string, params?: Record<string, string | number>) => translate(locale, key, params);
}
